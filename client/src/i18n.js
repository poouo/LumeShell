export function createTranslator(language = 'zh-CN') {
  const dict = messages[language] || messages['zh-CN'];
  return (key, params = {}) => {
    const template = dict[key] || messages['zh-CN'][key] || key;
    return Object.entries(params).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, value), template);
  };
}
